import math
import numpy as np
from math import comb
from scipy.stats import binom


data=[100,80,75,77,89,33,45,25,65,17,30,24,57,55,70,
 75,65,84,90,150]
n=len(data)
print(n)
#–°—Ä–µ–¥–Ω–µ–µ –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–æ–µ
mean=sum(data)/n
print(f"–°—Ä–µ–¥–Ω–µ–µ –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–æ–µ:{mean:.2f}")
#–°—Ä–µ–¥–Ω–µ–µ –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç, –Ω–∞—Å–∫–æ–ª—å–∫–æ –¥–∞–ª–µ–∫–æ –Ω–∞–±–ª—é–¥–µ–Ω–∏—è –º–æ–≥—É—Ç –±—ã—Ç—å "—Ä–∞–∑–±—Ä–æ—Å–∞–Ω—ã" –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ –∏—Ö —Å—Ä–µ–¥–Ω–µ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è ùúá


print(f" –°–º–µ—â–µ–Ω–Ω–æ–µ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ {np.std(data):.2f}")

#–°–º–µ—â–µ–Ω–Ω–∞—è–¥–∏—Å–ø–µ—Ä—Å–∏—è
variance_biased=sum((x-mean)**2 for x in data)/n
print(f"–°–º–µ—â–µ–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è:{variance_biased:.2f}")
print(f" –°–º–µ—â–µ–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è: {np.var(data):.2f}")
print(f" –ùC–º–µ—â–µ–Ω–Ω–æ–µ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ {np.sqrt(variance_biased)}")



# –ù–µ—Å–º–µ—â–µ–Ω–Ω–∞—è–¥–∏—Å–ø–µ—Ä—Å–∏—è
variance_unbiased =sum((x-mean)**2 for x in data)/(n-1)
print(f" –°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ {np.std(data):.2f}")
print(f" –ù–µ—Å–º–µ—â–µ–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è: {np.var(data):.2f}")
print(f"–ù–µ—Å–º–µ—â–µ–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è:{variance_unbiased:.2f}")
print(f" –ù–µ—Å–º–µ—â–µ–Ω–Ω–æ–µ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ {np.sqrt(variance_unbiased)}")
print(f" –ù–µ—Å–º–µ—â–µ–Ω–Ω–æ–µ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ {np.std(data, ddof=1):.2f}")

# –ó–∞–¥–∞—á–∞2.
# –í –ø–µ—Ä–≤–æ–º —è—â–∏–∫–µ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è 8 –º—è—á–µ–π, –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö 5 - –±–µ–ª—ã–µ.–í–æ –≤—Ç–æ—Ä–æ–º —è—â–∏–∫–µ - 12
# –º—è—á–µ–π, –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö 5 –±–µ–ª—ã—Ö.–ò–∑ –ø–µ—Ä–≤–æ–≥–æ —è—â–∏–∫–∞ –≤—ã—Ç–∞—Å–∫–∏–≤–∞—é—Ç —Å–ª—É—á–∞–π–Ω—ã–º –æ–±—Ä–∞–∑–æ–º –¥–≤–∞ –º—è—á–∞, –∏–∑
# –≤—Ç–æ—Ä–æ–≥–æ - 4. –ö–∞–∫–æ–≤–∞ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ, —á—Ç–æ3–º—è—á–∞–±–µ–ª—ã–µ?


# –î–∞–Ω–Ω—ã–µ –∑–∞–¥–∞—á–∏
total_balls_box1 = 8
white_balls_box1 = 5
total_balls_box2 = 12
white_balls_box2 = 5
# –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏
prob_two_white_box1 = comb(white_balls_box1, 2) / comb(total_balls_box1, 2)
prob_one_white_box2 = comb(white_balls_box2, 1) * comb(total_balls_box2- white_balls_box2, 3) / comb(total_balls_box2, 4)
print(prob_two_white_box1,prob_one_white_box2)
# –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ, —á—Ç–æ 3 –º—è—á–∞ –±–µ–ª—ã–µ
prob_three_white = prob_two_white_box1 * prob_one_white_box2
print(f"–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ, —á—Ç–æ 3 –º—è—á–∞ –±–µ–ª—ã–µ: {prob_three_white:.5f}")

# –ù–∞—Å–æ—Ä–µ–≤–Ω–æ–≤–∞–Ω–∏–∏ –ø–æ –±–∏–∞—Ç–ª–æ–Ω—É –æ–¥–∏–Ω –∏–∑ —Ç—Ä–µ—Ö —Å–ø–æ—Ä—Ç—Å–º–µ–Ω–æ–≤ —Å—Ç—Ä–µ–ª—è–µ—Ç –∏ –ø–æ–ø–∞–¥–∞–µ—Ç –≤
#  –º–∏—à–µ–Ω—å. –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –ø–æ–ø–∞–¥–∞–Ω–∏—è –¥–ª—è –ø–µ—Ä–≤–æ–≥–æ —Å–ø–æ—Ä—Ç—Å–º–µ–Ω–∞ —Ä–∞–≤–Ω–∞ 0.9, –¥–ª—è –≤—Ç–æ—Ä–æ–≥–æ
#  ‚Äî0.8, –¥–ª—è —Ç—Ä–µ—Ç—å–µ–≥–æ ‚Äî 0.6. –ù–∞–π—Ç–∏ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ, —á—Ç–æ –≤—ã—Å—Ç—Ä–µ–ª –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω:
#  a). –ø–µ—Ä–≤—ã–º —Å–ø–æ—Ä—Ç—Å–º–µ–Ω–æ–º
#  –±). –≤—Ç–æ—Ä—ã–º —Å–ø–æ—Ä—Ç—Å–º–µ–Ω–æ–º
#  –≤). —Ç—Ä–µ—Ç—å–∏–º —Å–ø–æ—Ä—Ç—Å–º–µ–Ω–æ–º.

p1 = 0.9
p2 = 0.8
p3 = 0.6
# –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏—Ç–æ–≥–æ,—á—Ç–æ–≤—ã—Å—Ç—Ä–µ–ª–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∫–∞–∂–¥—ã–º—Å–ø–æ—Ä—Ç—Å–º–µ–Ω–æ–º
prob1 = p1 / (p1 + p2 + p3)
prob2 = p2 / (p1 + p2 + p3)
prob3 = p3 / (p1 + p2 + p3)
print(f"–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ,—á—Ç–æ –≤—ã—Å—Ç—Ä–µ–ª –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω –ø–µ—Ä–≤—ã–º —Å–ø–æ—Ä—Ç—Å–º–µ–Ω–æ–º: {prob1: .5f}")
print(f"–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ,—á—Ç–æ –≤—ã—Å—Ç—Ä–µ–ª –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω –≤—Ç–æ—Ä—ã–º —Å–ø–æ—Ä—Ç—Å–º–µ–Ω–æ–º: {prob2: .5f}")
print(f"–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ,—á—Ç–æ –≤—ã—Å—Ç—Ä–µ–ª –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω —Ç—Ä–µ—Ç—å–∏–º —Å–ø–æ—Ä—Ç—Å–º–µ–Ω–æ–º: {prob3: .5f}")

# –í —É–Ω–∏–≤–µ—Ä—Å–∏—Ç–µ—Ç –Ω–∞ —Ñ–∞–∫—É–ª—å—Ç–µ—Ç—ã A –∏ B –ø–æ—Å—Ç—É–ø–∏–ª–æ —Ä–∞–≤–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ—Å —Ç—É–¥–µ–Ω—Ç–æ–≤, –∞ –Ω–∞ —Ñ–∞–∫—É–ª—å—Ç–µ—Ç C —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ –ø–æ—Å—Ç—É–ø–∏–ª–æ —Å—Ç–æ–ª—å–∫–æ–∂–µ,
# —Å–∫–æ–ª—å–∫–æ –Ω–∞ A –∏ B –≤–º–µ—Å—Ç–µ. –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å # —Ç–æ–≥–æ, —á—Ç–æ —Å—Ç—É–¥–µ–Ω—Ç —Ñ–∞–∫—É–ª—å—Ç–µ—Ç–∞ A —Å–¥–∞—Å—Ç –ø–µ—Ä–≤—É—é —Å–µ—Å—Å–∏—é, —Ä–∞–≤–Ω–∞ 0
# .8.–î–ª—è—Å—Ç—É–¥–µ–Ω—Ç–∞
# —Ñ–∞–∫—É–ª—å—Ç–µ—Ç–∞B—ç—Ç–∞–≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å—Ä–∞–≤–Ω–∞0
# .7, –∞–¥–ª—è—Å—Ç—É–¥–µ–Ω—Ç–∞—Ñ–∞–∫—É–ª—å—Ç–µ—Ç–∞C - 0.9.–°—Ç—É–¥–µ–Ω—Ç
# —Å–¥–∞–ª–ø–µ—Ä–≤—É—é—Å–µ—Å—Å–∏—é.–ö–∞–∫–æ–≤–∞–≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å, —á—Ç–æ–æ–Ω—É—á–∏—Ç—Å—è:
# a).–Ω–∞—Ñ–∞–∫—É–ª—å—Ç–µ—Ç–µA
# –±).–Ω–∞—Ñ–∞–∫—É–ª—å—Ç–µ—Ç–µB
# –≤).–Ω–∞—Ñ–∞–∫—É–ª—å—Ç–µ—Ç–µC?

 # –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏ —Å–¥–∞—á–∏ —Å–µ—Å—Å–∏–∏
pA = 0.8
pB = 0.7
pC = 0.9
# –ü—Ä–æ–ø–æ—Ä—Ü–∏–∏ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤
total_students = 2 + 2 + 4 # —Ñ–∞–∫—É–ª—å—Ç–µ—Ç C –∏–º–µ–µ—Ç –≤ –¥–≤–∞ —Ä–∞–∑–∞ –±–æ–ª—å—à–µ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤, —á–µ–º A –∏ B –≤–º–µ—Å—Ç–µ
prob_A = 2 / total_students
prob_B=2/total_students
prob_C=4/total_students
#–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏—Å–¥–∞—á–∏—Å–µ—Å—Å–∏–∏–¥–ª—è—Ñ–∞–∫—É–ª—å—Ç–µ—Ç–æ–≤
prob_pass_A=prob_A*pA
prob_pass_B=prob_B*pB
prob_pass_C=prob_C*pC
#–û–±—â–∞—è–≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å—Å–¥–∞—á–∏—Å–µ—Å—Å–∏–∏
total_prob=prob_pass_A+prob_pass_B+prob_pass_C
#–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏–¥–ª—è–∫–∞–∂–¥–æ–≥–æ—Ñ–∞–∫—É–ª—å—Ç–µ—Ç–∞
prob_A_given_pass=prob_pass_A/total_prob
prob_B_given_pass=prob_pass_B/total_prob
prob_C_given_pass=prob_pass_C/total_prob
print(f"–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å,—á—Ç–æ —Å—Ç—É–¥–µ–Ω—Ç —É—á–∏—Ç—Å—è –Ω–∞ —Ñ–∞–∫—É–ª—å—Ç–µ—Ç–µ A:{prob_A_given_pass:.5f}")
print(f"–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å,—á—Ç–æ —Å—Ç—É–¥–µ–Ω—Ç —É—á–∏—Ç—Å—è –Ω–∞ —Ñ–∞–∫—É–ª—å—Ç–µ—Ç–µ B:{prob_B_given_pass:.5f}")
print(f"–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å,—á—Ç–æ —Å—Ç—É–¥–µ–Ω—Ç —É—á–∏—Ç—Å—è –Ω–∞ —Ñ–∞–∫—É–ª—å—Ç–µ—Ç–µ C:{prob_C_given_pass:.5f}")